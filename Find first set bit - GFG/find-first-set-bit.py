class Solution:
    
    #Function to find position of first set bit in the given number.
    def getFirstSetBit(self,n):
        #Your code here
        if n==0:
            return 0
        count=0
        t=bin(n)[2:]
        count=1
        for i in range(len(t)-1,-1,-1):
            if t[i]=='1':
                return count
            count+=1
            

